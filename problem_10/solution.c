#include <stdio.h>

/*
 * 题目：温度转换
 * 
 * 题目描述：
 * 输入一个华氏温度，要求输出摄氏温度。
 * 转换公式：C = (F - 32) * 5/9
 * 
 * 思路：
 * 1. 读取华氏温度F
 * 2. 使用公式 C = (F - 32) * 5.0 / 9 计算摄氏温度
 * 3. 保留两位小数输出结果
 */

int main() {
    double f;
    scanf("%lf", &f);
    double c = (f - 32) * 5.0 / 9.0;
    printf("%.2f\n", c);
    return 0;
}
