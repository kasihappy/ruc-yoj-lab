#include <stdio.h>

/*
 * 题目：切换字符大小写
 * 
 * 题目描述：
 * 读入一个字母，切换其大小写并输出。
 * 
 * 思路：
 * 1. 读取一个字符
 * 2. 判断字符是大写还是小写
 * 3. 大写转小写(+32)，小写转大写(-32)
 * 4. 输出转换后的字符
 */

int main() {
    char c;
    scanf("%c", &c);
    if (c >= 'a' && c <= 'z') {
        // 小写转大写
        c = c - 32;
    } else if (c >= 'A' && c <= 'Z') {
        // 大写转小写
        c = c + 32;
    }
    printf("%c\n", c);
    return 0;
}
